
public class Professor {

    public String nome;
    public String disciplina;
    public int[] vDisciplina;
    public int[] vNome;

    public Professor(String nome, String disciplina, int[] vNome, int[] vDisciplina) {
        this.nome = nome;
        this.disciplina = disciplina;
        this.vDisciplina = vDisciplina;
        this.vNome = vNome;
    }

}
